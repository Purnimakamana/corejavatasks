package datatypesoperators;

public class Datatypes {
	public static void main(String[] args) {
		int num1=10;//integer datatype
		System.out.println(num1);
		float num2=10.7f;//float datatype
		System.out.println(num2);
		double num3=234d;//double datatype
		System.out.println(num3);
		char letter='P';//character datatype
		System.out.println(letter);
		boolean one=false;//boolean datatype
		System.out.println(one);
		byte a=10;//byte datatype
		System.out.println(a);
		short s=1000;//short datatype
		System.out.println(s);
		long num4=10000l;//long datatype
		System.out.println(num4);
	}

}
